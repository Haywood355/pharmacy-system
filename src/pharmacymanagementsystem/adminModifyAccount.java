    private void searchUsernameButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchUsernameButtonActionPerformed
        clearProfile();
        
        String searchUsername = searchUsernameField.getText();
        if (Validation.isTrimEmpty(searchUsername)) {
            JOptionPane.showMessageDialog(null, "To begin searching, please enter a username.", "Opps!", JOptionPane.ERROR_MESSAGE);                  
            return;
        }
        
        if (!client.isUsernameExist(searchUsername)) {
            JOptionPane.showMessageDialog(null, "Username does not exist in the record. Please try again.", "Opps!", JOptionPane.ERROR_MESSAGE);                              
            return;
        }
        
        // If username exists in the record, get their data and fill up the form
        Account user = client.getAccount(searchUsername);

        roleCombo.setSelectedItem(user.getRoleName()); 
        usernameField.setText(searchUsername);
        nameField.setText(user.getName());
        emailField.setText(user.getEmail());
    }//GEN-LAST:event_searchUsernameButtonActionPerformed

