    private void updateAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAccountButtonActionPerformed
        String username = usernameField.getText();
        if (Validation.isTrimEmpty(username)) {
            JOptionPane.showMessageDialog(null, "Please search a username first.", "Opps!", JOptionPane.ERROR_MESSAGE);                  
            return;
        }
        
        if (!client.isUsernameExist(username)) {
            JOptionPane.showMessageDialog(null, "Username was not found. \nPlease try again.", "Opps!", JOptionPane.ERROR_MESSAGE);                              
            return;
        }

        String roleName = roleCombo.getSelectedItem().toString();
        String name = nameField.getText();
        String email = emailField.getText();
 
        client.updateRole(username, Role.toInt(roleName));
        client.updateName(username, name);
        client.updateEmail(username, email);
        
        JOptionPane.showMessageDialog(null, "Successfully updated " + username + " account.", "Hooray!", JOptionPane.INFORMATION_MESSAGE);                              
       
    }//GEN-LAST:event_updateAccountButtonActionPerformed

