    private void deleteAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteAccountButtonActionPerformed
        boolean success = false;
        String username;
        
        int dialogResult = JOptionPane.showConfirmDialog(null, "Are you sure you want to delete this account? \nThe action is irreversible and will have permanent effect.", "Warning!", JOptionPane.YES_NO_OPTION);
        
        if (dialogResult == JOptionPane.YES_OPTION) {
            username = searchUsernameField.getText();
            success = client.deleteAccount(username);
        } else {
            return;
        }
        
        if (success) {
            JOptionPane.showMessageDialog(null, "The account (Username: "+username+") has been deleted permanently.", "Heads up!", JOptionPane.INFORMATION_MESSAGE);                              
            clearProfile(true);
        } else {
            JOptionPane.showMessageDialog(null, "The account failed to be deleted or the username does not exists in the record.", "Opps!", JOptionPane.ERROR_MESSAGE);                                          
        }
        
    }//GEN-LAST:event_deleteAccountButtonActionPerformed

    
    private void clearProfile(boolean resetSearchUsername) {
        if (resetSearchUsername) searchUsernameField.setText("");
        
        clearProfile();
    }
    
    private void clearProfile() {
        usernameField.setText("");
        nameField.setText("");
        emailField.setText("");
        roleCombo.setSelectedIndex(0);
    }    

