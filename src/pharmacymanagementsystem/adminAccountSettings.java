    private void updateAccountButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateAccountButtonActionPerformed
        String name = nameField.getText();
        String email = emailField.getText();
        
        if (Validation.isTrimEmpty(name)) {
            JOptionPane.showMessageDialog(null, "Please enter name.", "Opps!", JOptionPane.ERROR_MESSAGE);            
            return;
        }
        
        if (Validation.isTrimEmpty(email)) {
            JOptionPane.showMessageDialog(null, "Please enter email.", "Opps!", JOptionPane.ERROR_MESSAGE);            
            return;
        } else if (!Validation.isEmail(email)) {
            JOptionPane.showMessageDialog(null, "Email is invalid. \n\nExample: \nsomeone@email.com", "Opps!", JOptionPane.ERROR_MESSAGE);      
            return;            
        }
        
        if (client.updateName(name) && client.updateEmail(email)) {
            JOptionPane.showMessageDialog(null, "Account updated successfully!", "Hooray!", JOptionPane.INFORMATION_MESSAGE);                 
        } else {
            JOptionPane.showMessageDialog(null, "Something went wrong...\nFailed to update account.", "Opps!", JOptionPane.ERROR_MESSAGE);                  
        }
    }//GEN-LAST:event_updateAccountButtonActionPerformed

