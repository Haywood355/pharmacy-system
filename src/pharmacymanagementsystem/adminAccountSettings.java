    private void updatePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatePasswordButtonActionPerformed
        String password = passwordField.getText();
        String cpassword = cpasswordField.getText();
        
        if (Validation.isTrimEmpty(password)) {
            JOptionPane.showMessageDialog(null, "Please enter password.", "Opps!", JOptionPane.ERROR_MESSAGE);                 
            return;
        } 
        else if (password.length() < 8) {
            JOptionPane.showMessageDialog(null, "The password must be at least 8 characters.", "Opps!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (Validation.isTrimEmpty(cpassword)) {
            JOptionPane.showMessageDialog(null, "Please enter confirmation password.", "Opps!", JOptionPane.ERROR_MESSAGE);
            return;
        } 
        else if (!cpassword.equals(password)) {
            JOptionPane.showMessageDialog(null, "The confirmation password did not match.", "Opps!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if (client.updatePassword(password)) {
            JOptionPane.showMessageDialog(null, "Password updated successfully!", "Hooray!", JOptionPane.INFORMATION_MESSAGE);                 
            
            String message = client.logout();    
            new login(message).setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(null, "Something went wrong...\nFailed to update password.", "Opps!", JOptionPane.ERROR_MESSAGE);                  
        }
    }//GEN-LAST:event_updatePasswordButtonActionPerformed

